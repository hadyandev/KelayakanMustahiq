    private String number(int x) {
        String num = "Rp. " + NumberFormat.getIntegerInstance().format(x) + ",-";
        return num;
    }

    private String keterangan(float x) {
        String keterangan;
        if (x > 60) {
            keterangan = "Layak";
        } else {
            keterangan = "Tidak Layak";
        }
        return keterangan;
    }

    private Float decimal(float derajatInput) {
        DecimalFormat decimal = new DecimalFormat();
        decimal.setMaximumFractionDigits(2);
        String derajatProccess = decimal.format(derajatInput);
        float derajatOutput = Float.valueOf(derajatProccess);
        return derajatOutput;
    }

    private void FillData() {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("#");
        dtm.addColumn("Nama Mustahiq");
        dtm.addColumn("Tanggungan");
        dtm.addColumn("Kualitas Tempat Tinggal");
        dtm.addColumn("Pendapatan");
        dtm.addColumn("Attitude");
        dtm.addColumn("Kelayakan");
        dtm.addColumn("Keterangan");

        ArrayList list = new ArrayList();
        for (Mustahiq cm : this.cmm.findAll()) {
            list.add(cm.getIdMustahiq());
        }

        for (Mustahiq cm : this.cmm.findAll()) {
            int x = list.indexOf(cm.getIdMustahiq()) + 1;
            dtm.addRow(new Object[]{x, cm.getNamaMustahiq(), cm.getJumlahTanggungan(), cm.getKualitasTempattinggal(),
                number(cm.getJumlahPendapatan()), cm.getAttitude(), cm.getKelayakan(), cm.getStatus()});
        }
        this.tblKelayakan.setModel(dtm);
        tblKelayakan.setRowSorter(new TableRowSorter(dtm));
    }

