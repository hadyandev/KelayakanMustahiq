    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        // TODO add your handling code here:

        MembershipFunction mf = new MembershipFunction();
        Mustahiq mustahiq = new Mustahiq();
        Tsukamoto tsukamoto = new Tsukamoto();
        FunctionMustahiq fm = new FunctionMustahiq();
        try {
            for (Mustahiq m : this.cmm.findAll()) {
                int id = m.getIdMustahiq();
                m = this.cmm.find(id);

                mustahiq.setIdMustahiq(id);
                mustahiq.setNamaMustahiq(fm.namaMustahiq(id));
                mustahiq.setAlamat(fm.alamat(id));
                mustahiq.setJumlahTanggungan(fm.jumlahTanggungan(id));
                mustahiq.setKualitasTempattinggal(fm.tempatTinggal(id));
                mustahiq.setJumlahPendapatan(fm.jumlahPendapatan(id));
                mustahiq.setAttitude(fm.attitude(id));
                mustahiq.setKelayakan(decimal(tsukamoto.z(id)));
                mustahiq.setStatus(keterangan(tsukamoto.z(id)));
                this.cmm.update(mustahiq);
            }
        } catch (Exception e) {
        }
        FillData();
    }//GEN-LAST:event_btnRefreshActionPerformed

